package JAVA_Practice.patterns;

public class pt5 {
    public static void main(String[] args) {
        int n = 5;
        for(int i = 0; i<n;i++)
        {
            for(int j = 0; j < n;j++)
            {
                System.out.print(j+1+" ");
            }
            System.out.println();
        }
    }
}
